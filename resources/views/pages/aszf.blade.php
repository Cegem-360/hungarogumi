        szolgáltatás nyújtásra vonatkozó – szerződést, köteles a felmondás vállalkozással való közlése időpontjáig
        teljesített szolgáltatással arányos díjat a vállalkozás számára megfizetni. A fogyasztó által arányosan
        fizetendő összeget a szerződésben megállapított ellenszolgáltatás adóval növelt teljes összege alapján kell
        megállapítani. Ha a fogyasztó bizonyítja, hogy az ily módon megállapított teljes összeg túlzottan magas, az
        arányos összeget a szerződés megszűnésének időpontjáig teljesített szolgáltatások piaci értéke alapján kell
        kiszámítani. Kérjük vegye figyelembe, hogy utánvéttel vagy portósan visszaküldött terméket nem áll módunkban
        átvenni.</p>
    <h3><strong>Fogyasztó felelőssége az értékcsökkenésért</strong></h3>
    <p>A fogyasztó a termék jellegének, tulajdonságainak és működésének megállapításához szükséges használatot meghaladó
        használatból eredő értékcsökkenésért felel.</p>
    <h2><strong>Az elállási jog az alábbi esetekben nem gyakorolható</strong></h2>
    <p>Az Eladó kifejezetten felhívja az Ön figyelmét, hogy Ön nem gyakorolhatja elállási jogát a 45/2014 (II.26.) Korm.
        Rendelet 29. §. (1) bekezdésében foglalt esetekben:</p>
    <p><em>a) </em>a szolgáltatás nyújtására irányuló szerződés esetében a szolgáltatás egészének teljesítését követően,
        ha a vállalkozás a teljesítést a fogyasztó kifejezett, előzetes beleegyezésével kezdte meg, és a fogyasztó
        tudomásul vette, hogy a szolgáltatás egészének teljesítését követően felmondási jogát elveszíti;</p>
    <p><em>b) </em>olyan termék vagy szolgáltatás tekintetében, amelynek ára, illetve díja a pénzpiac vállalkozás által
        nem befolyásolható, az elállási jog gyakorlására meghatározott határidő alatt is lehetséges ingadozásától függ;
    </p>
    <p><em>c) </em>olyan nem előre gyártott termék esetében, amelyet a fogyasztó utasítása alapján vagy kifejezett
        kérésére állítottak elő, vagy olyan termék esetében, amelyet egyértelműen a fogyasztó személyére szabtak;</p>
    <p><em>d) </em>romlandó vagy minőségét rövid ideig megőrző termék tekintetében;</p>
    <p><em>e) </em>olyan zárt csomagolású termék tekintetében, amely egészségvédelmi vagy higiéniai okokból az átadást
        követő felbontása után nem küldhető vissza;</p>
    <p><em>f) </em>olyan termék tekintetében, amely jellegénél fogva az átadást követően elválaszthatatlanul vegyül más
        termékkel;</p>
    <p><em>g) </em>olyan alkoholtartalmú ital tekintetében, amelynek tényleges értéke a vállalkozás által nem
        befolyásolható módon a piaci ingadozásoktól függ, és amelynek áráról a felek az adásvételi szerződés
        megkötésekor állapodtak meg, azonban a szerződés teljesítésére csak a megkötéstől számított harmincadik napot
        követően kerül sor;</p>
    <p><em>h)</em> olyan vállalkozási szerződés esetében, amelynél a vállalkozás a fogyasztó kifejezett kérésére keresi
        fel a fogyasztót sürgős javítási vagy karbantartási munkálatok elvégzése céljából;</p>
    <p><em>i) </em>lezárt csomagolású hang-, illetve képfelvétel, valamint számítógépes szoftver példányának adásvétele
        tekintetében, ha az átadást követően a fogyasztó a csomagolást felbontotta;</p>
    <p><em>j) </em>hírlap, folyóirat és időszaki lap tekintetében, az előfizetéses szerződések kivételével;</p>
    <p><em>k) </em>nyilvános árverésen megkötött szerződések esetében;</p>
    <p><em>l) </em>lakáscélú szolgáltatás kivételével szállásnyújtásra irányuló szerződés, fuvarozás,
        személygépjármű-kölcsönzés, étkeztetés vagy szabadidős tevékenységekhez kapcsolódó szolgáltatásra irányuló
        szerződés esetében, ha a szerződésben meghatározott teljesítési határnapot vagy határidőt kötöttek ki;</p>
    <p><em>m) </em>a nem tárgyi adathordozón nyújtott digitális adattartalom tekintetében, ha a vállalkozás a fogyasztó
        kifejezett, előzetes beleegyezésével kezdte meg a teljesítést, és a fogyasztó e beleegyezésével egyidejűleg
        nyilatkozott annak tudomásul vételéről, hogy a teljesítés megkezdését követően elveszíti az elállási jogát.</p>
    <h2><strong>Kellékszavatosság, termékszavatosság, jótállás</strong></h2>
    <p>A fogyasztói tájékoztató jelen pontja a 45/2014 (II.26.) Korm. rendelet 9. § (3) bekezdése felhatalmazása alapján
        a 45/2014 (II.26.) Korm. rendelet 3. számú melléklete alkalmazásával készült.</p>
    <h2><strong>Kellékszavatosság</strong></h2>
    <p><strong>Milyen esetben élhet Ön a kellékszavatossági jogával?</strong>
    </p>
    <p>Ön az Eladó hibás teljesítése esetén az Eladóval szemben kellékszavatossági igényt érvényesíthet a Polgári
        Törvénykönyv szabályai szerint.</p>
    <p><strong>Milyen jogok illetik meg Önt kellékszavatossági igénye alapján?</strong>
    </p>
    <p>Ön – választása szerint–az alábbi kellékszavatossági igényekkel élhet:</p>
    <p>Kérhet kijavítást vagy kicserélést, kivéve, ha az ezek közül az Ön által választott igény teljesítése lehetetlen
        vagy az Eladó számára más igénye teljesítéséhez képest aránytalan többletköltséggel járna. Ha a kijavítást vagy
        a kicserélést nem kérte, illetve nem kérhette, úgy igényelheti az ellenszolgáltatás arányos leszállítását vagy a
        hibát az Eladó költségére Ön is kijavíthatja, illetve mással kijavíttathatja vagy – végső esetben – a
        szerződéstől is elállhat.</p>
    <p>Választott kellékszavatossági jogáról egy másikra is áttérhet, az áttérés költségét azonban Ön viseli, kivéve, ha
        az indokolt volt, vagy arra az Eladó adott okot.</p>
    <p><strong>Milyen határidőben érvényesítheti Ön kellékszavatossági igényét?</strong>
    </p>
    <p>Ön köteles a hibát annak felfedezése után haladéktalanul, de nem később, mint a hiba felfedezésétől számított
        kettő hónapon belül közölni. Ugyanakkor felhívjuk a figyelmét, hogy a szerződés teljesítésétől számított két
        éves elévülési határidőn túl kellékszavatossági jogait már nem érvényesítheti.
        <br>Ha a fogyasztó és a vállalkozás közötti szerződés tárgya használt dolog, a felek rövidebb elévülési időben
        is megállapodhatnak; egy évnél rövidebb elévülési határidő ebben az esetben sem köthető ki érvényesen.
    </p>
    <p><strong>Kivel szemben érvényesítheti kellékszavatossági igényét?</strong>
    </p>
    <p>Ön az Eladóval szemben érvényesítheti kellékszavatossági igényét.</p>
    <p><strong>Milyen egyéb feltétele van kellékszavatossági jogai érvényesítésének?</strong>
    </p>
    <p>A teljesítéstől számított hat hónapon belül a kellékszavatossági igénye érvényesítésének a hiba közlésén túl
        nincs egyéb feltétele, ha Ön igazolja, hogy a terméket, illetve a szolgáltatást az Eladó nyújtotta. A
        teljesítéstől számított hat hónap eltelte után azonban már Ön köteles bizonyítani, hogy az Ön által felismert
        hiba már a teljesítés időpontjában is megvolt.</p>
    <p>Használt termékek esetén alapesetben az általános szabályoktól eltérően alakulnak a szavatossági, jótállási
        jogok. A használt termékek esetében is beszélhetünk hibás teljesítésről, azonban figyelembe kell venni azokat a
        körülményeket, amelyek alapján a Vásárló bizonyos hibák előfordulására számíthatott. Az avulás folytán ugyanis
        egyes hibák jelentkezései egyre gyakoribbakká válnak, amelyek következtében azt, hogy egy használt termék
        ugyanolyan minőséggel rendelkezhet, mint egy újonnan vásárolt, nem lehet feltételezni. Ennek alapján a Vásárló
        csak az olyan hiányosságok tekintetében érvényesítheti a szavatossági jogait, amelyek a használtságból eredő
        hibákon felüliek, és azoktól függetlenül keletkeztek. Ha a használt termék hibás és erről a Fogyasztónak
        minősülő Vásárló a vásárláskor tájékoztatást kapott, az ismert hiba vonatkozásában a Szolgáltatónak nincs
        felelőssége.</p>
    <h2><strong>Termékszavatosság</strong></h2>
    <p><strong>Milyen esetben élhet Ön a termékszavatossági jogával?</strong>
    </p>
    <p>Ingó dolog (termék) hibája esetén Ön – választása szerint – a kellékszavatossági igényt vagy termékszavatossági
        igényt érvényesíthet.</p>
    <p><strong>Milyen jogok illetik meg Önt termékszavatossági igénye alapján?</strong>
    </p>
    <p>Termékszavatossági igényként Ön kizárólag a hibás termék kijavítását vagy kicserélését kérheti.</p>
    <p><strong>Milyen esetben minősül a termék hibásnak?</strong>
    </p>
    <p>A termék akkor hibás, ha az nem felel meg a forgalomba hozatalakor hatályos minőségi követelményeknek vagy pedig,
        ha nem rendelkezik a gyártó által adott leírásban szereplő tulajdonságokkal.</p>
    <p><strong>Milyen határidőben érvényesítheti Ön termékszavatossági igényét?</strong>
    </p>
    <p>Termékszavatossági igényét Ön a termék gyártó általi forgalomba hozatalától számított két éven belül
        érvényesítheti. E határidő elteltével e jogosultságát elveszti.</p>
    <p><strong>Kivel szemben és milyen egyéb feltétellel érvényesítheti termékszavatossági igényét?</strong>
    </p>
    <p>Termékszavatossági igényét kizárólag az ingó dolog gyártójával vagy forgalmazójával szemben gyakorolhatja. A
        termék hibáját termékszavatossági igény érvényesítése esetén Önnek kell bizonyítania.</p>
    <p><strong>A gyártó (forgalmazó) milyen esetben mentesül termékszavatossági kötelezettsége alól?</strong>
    </p>
    <p>A gyártó (forgalmazó) kizárólag akkor mentesül termékszavatossági kötelezettsége alól, ha bizonyítani tudja,
        hogy:</p>
    <ul>
        <li>
            <p>a terméket nem üzleti tevékenysége körében gyártotta, illetve hozta forgalomba, vagy</p>
        </li>
        <li>
            <p>a hiba a tudomány és a technika állása szerint a forgalomba hozatal időpontjában nem volt felismerhető
                vagy</p>
        </li>
        <li>
            <p>a termék hibája jogszabály vagy kötelező hatósági előírás alkalmazásából ered.</p>
        </li>
    </ul>
    <p>A gyártónak (forgalmazónak) a mentesüléshez elegendő egy okot bizonyítania.</p>
    <p>Felhívom figyelmét, hogy ugyanazon hiba miatt kellékszavatossági és termékszavatossági igényt egyszerre,
        egymással párhuzamosan nem érvényesíthet. Termékszavatossági igényének eredményes érvényesítése esetén azonban a
        kicserélt termékre, illetve kijavított részre vonatkozó kellékszavatossági igényét a gyártóval szemben
        érvényesítheti.</p>
    <h2><strong>Jótállás</strong></h2>
    <p><strong>Milyen esetben élhet Ön a jótállási jogával?</strong>Hibás teljesítés esetén szerződés, vagy jogszabályi
        előírás alapján a kötelezett a köznyelvben garanciaként ismert fogalommal megegyező jótállásra köteles.</p>
    <p>Jogszabály írja elő a jótállás biztosítását az olyan tartós fogyasztási cikkek (pl.: műszaki cikkek, szerszámok,
        gépek) és alkatrészeik esetében, amelyek vásárlási értéke a 10 000 Ft-ot meghaladja.</p>
    <p>Szerződéses alapú a jótállás, ha a kötelezett (akár a gyártó, akár a kereskedő) vállal saját maga, az általa
        meghatározott feltételek szerint jótállást a termékre. Ilyen esetben a jótállási (garancia) feltételeket kell
        elolvasni a jótállással kapcsolatban és a jótálláshoz jótállási jegy is jár.</p>
    <p><strong>Önt milyen jogok és milyen határidőn belül illetik meg jótállás alapján?</strong>
    </p>
    <p>Az egyes tartós fogyasztási cikkekre vonatkozó kötelező jótállásról szóló 151/2003. (IX. 22.) Korm. rendelet
        határozza meg a kötelező jótállás eseteit. Az ezen esetkörbe nem tartozó Termékek esetén az Eladó jótállást nem
        vállal. A jótállási igény a jótállási határidőben érvényesíthető. Ha a jótállásra kötelezett kötelezettségének a
        jogosult felhívására – megfelelő határidőben – nem tesz eleget, a jótállási igény a felhívásban tűzött határidő
        elteltétől számított három hónapon belül akkor is érvényesíthető bíróság előtt, ha a jótállási idő már eltelt. E
        határidő elmulasztása jogvesztéssel jár. A jótállási igény érvényesítésére egyebekben a kellékszavatossági jogok
        gyakorlására vonatkozó szabályokat kell megfelelően alkalmazni. A jótállás időtartama egy év. E határidő
        elmulasztása jogvesztéssel jár. A jótállási határidő a fogyasztási cikk fogyasztó részére történő átadása, vagy
        ha az üzembe helyezést a vállalkozás vagy annak megbízottja végzi, az üzembe helyezés napjával kezdődik. Az egy
        éven túli esetleges jótállási igényekkel kapcsolatban szíveskedjen a gyártóhoz fordulni!</p>
